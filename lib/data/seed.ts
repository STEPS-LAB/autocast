export function getProductCards(): ProductCard[] {
  return PRODUCTS.map(p => ({
    id: p.id,
    slug: p.slug,
    name_ua: p.name_ua,
    price: p.price,
    sale_price: p.sale_price,
    images: p.images,
    stock: p.stock,
    category: CATEGORIES.find(c => c.id === p.category_id)
      ? { name_ua: CATEGORIES.find(c => c.id === p.category_id)!.name_ua, slug: CATEGORIES.find(c => c.id === p.category_id)!.slug }
      : undefined,
    brand: BRANDS.find(b => b.id === p.brand_id)
      ? { name: BRANDS.find(b => b.id === p.brand_id)!.name }
      : undefined,
  }))
}

export function getFeaturedProducts(): ProductCard[] {
  return getProductCards().filter((_, i) => PRODUCTS[i]?.is_featured)
}

export function getProductBySlug(slug: string): Product | undefined {
  return PRODUCTS.find(p => p.slug === slug)
}

export function getProductsByCategory(categorySlug: string): ProductCard[] {
  const cat = CATEGORIES.find(c => c.slug === categorySlug)
  if (!cat) return []
  return getProductCards().filter((_, i) => PRODUCTS[i]?.category_id === cat.id)
}

export function searchProducts(query: string): ProductCard[] {
  const q = query.toLowerCase().trim()
  if (!q) return []
  return getProductCards().filter((_, i) =>
    PRODUCTS[i]?.name_ua.toLowerCase().includes(q) ||
    PRODUCTS[i]?.description_ua.toLowerCase().includes(q)
  )
}

